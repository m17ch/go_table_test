package mymath

// Halves function divides input by 2
func Halves(n float64) float64 {
	return n / 2
}

// IsEven returns true if input is even number, otherwise false
func IsEven(n int) bool {
	return n%2 == 0
}

// Addition returns the sum of the two input integers
func Addition(m, n int) int {
	return m + n
}
